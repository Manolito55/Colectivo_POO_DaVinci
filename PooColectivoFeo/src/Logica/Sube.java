package Logica;

public class Sube {
	
	private int id;
	private Double credito;
	
	
	public Sube(int id, Double credito) {
		super();
		this.id = id;
		this.credito = credito;
	}
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Double getCredito() {
		return credito;
	}
	public void setCredito(Double credito) {
		this.credito = credito;
	}
	
	

}
